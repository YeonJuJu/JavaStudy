package package1;

/*
  접근제한자(access modifier)
  
  private < default < protected < public
  
  private   :    한 클래스 내부
  default   :    한 패키지 내부
  protected :    상속 관계에 있는 경우에만 패키지를 넘어서 접근 가능
  public    :    접근 제한이 없음
  
*/

public class Pac2Src1 {
  public    int num1;
  private   int num2;
            int num3;
  protected int num4;           
  
}







